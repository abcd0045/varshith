class stack:
    def __init__(self):
        self.items=[]
    def isempty(self):
        return self.items==[]
    def push(self,items):
        self:items.append(items)
        print(items)
    def pop(self):
        return self.items[len(self.items)-1]
    def size(self):
        return len(self.items)
s=stack()
print('stack operation examples')
print(s.isempty())
s.push(8.4)
